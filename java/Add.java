public class Add {
    public int x;
    public int y;
    
    public void setx(int x)
    {
        this.x=x;
    }
    public void sety(int y)
    {
        this.y=y;
    }
    public int getx()
    {
        return x;
    }
    public int gety()
    {
        return y;
    }
    public int sum()
    {
        return x+y;
    }
    
}
